class BookEntry {
    constructor(entryNumber,author, title, publication, pageCount, ISBN, seriesTitle, note, resource, languageCode){
        this.entryNumber = entryNumber;
        this.author = author;
        this.title = title;
        this.publication = publication;
        this.pageCount = pageCount;
        this.ISBN = ISBN;
        this.seriesTitle = seriesTitle;
        this.note = note;
        this.resource = resource;
        this.languageCode = languageCode;

    }
}